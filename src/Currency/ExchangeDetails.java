    //Sets the labels in the form with the details of the exchange transaction
    public void setExchangeDetails(ExchangeTransaction exchange) {
        String fromCurrency = exchange.getSourceCurrency();
        String toCurrency = exchange.getTargetCurrency();
        double initialAmount = exchange.getInitialAmount();
        double convertedAmount = exchange.getConvertedAmount();
        double processingFee = exchange.getProcessingFee();
        double totalAmount = exchange.getTotalAmountPaid();

        DecimalFormat df = new DecimalFormat("#0.00");
        lbl_fromCurrency.setText(fromCurrency);
        lbl_toCurrency.setText(toCurrency);
        lbl_initialAmount.setText(df.format(initialAmount)+" "+fromCurrency);
        lbl_convertedAmount.setText(df.format(convertedAmount)+" "+toCurrency);
        lbl_processFee.setText(df.format(processingFee)+" "+fromCurrency);
        lbl_totalAmount.setText(df.format(totalAmount) + " " + fromCurrency);
    }


