    /**
     * Opens a file explorer and selects a profile picture.
     * @param profilePic The JLabel to set the selected profile picture.
     * @return True if the file was successfully selected, otherwise false.
     */
    private boolean OpenFileViaExplorer(JLabel profilePic) {
        try {
            // Create a file chooser
            JFileChooser fileChooser = new JFileChooser();
            // Set file filter to only show image files
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Image files", "png", "jpg");
            fileChooser.setFileFilter(filter);
            // Set the current directory to the working directory
            fileChooser.setCurrentDirectory(new File("."));
            // Show the open dialog and get the user's action
            int result = fileChooser.showOpenDialog(null);
            System.out.println("Result" + result);
            
            // Check if the user approved the file selection
            if (result == JFileChooser.APPROVE_OPTION) {
                // Get the selected file
                File selectedFile = new File(fileChooser.getSelectedFile().getAbsolutePath());
                System.out.println("Filepath: " + selectedFile);
                
                String selectedfilepath=selectedFile.getAbsolutePath();
                // Create an ImageIcon from the selected file
                ImageIcon icon = new ImageIcon(selectedfilepath);

                // Set the ImageIcon as the icon for the profilePic component
                profilePic.setIcon(icon);
                // Update the user's avatar image path
                user.setUserAvatar(new UserAvatar(selectedfilepath,user.getUserID()));
                System.out.println("the path is:" +user.getUserAvatar().getImagePath());
                Connection connection = null;
                PreparedStatement selectStatement = null;
                ResultSet resultset=null;
                
                String userid, selectquery, updatequery,finduserid,oldImagePath;
                int notFound = 0;
                
                try {
                    // Load the MySQL JDBC driver
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    // Establish a connection to the database
                    connection = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/Egringotts", "root", "password");

                    userid=user.getUserID();
                    selectquery = "SELECT * FROM Account WHERE UserID= ?";
                    updatequery = "UPDATE Account SET ImagePath=? WHERE UserID=?";
                    
                    // Prepare and execute the select query
                    selectStatement = connection.prepareStatement(selectquery);
                    selectStatement.setString(1, userid);
                    resultset = selectStatement.executeQuery();
                    
                    while (resultset.next()) {
                        finduserid=resultset.getString("UserID");
                        oldImagePath = resultset.getString("ImagePath");
                        notFound = 1;

                        // Check if the user was found and update the profile picture path
                        if (notFound == 1 ) {
                            PreparedStatement updateStatement = connection.prepareStatement(updatequery);
                            updateStatement.setString(1, selectedfilepath);
                            updateStatement.setString(2, userid);
                            updateStatement.executeUpdate();
                            updateStatement.close();
                            // Show a success message
                            JOptionPane.showMessageDialog(new JFrame(), "Profile Picture change successfully!");

                        } else {
                            // Show an error message if the update failed
                            JOptionPane.showMessageDialog(new JFrame(), "Profile Picture change unsuccessfully!", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    // Show an error message if the user was not found
                    if (notFound == 0) {
                        JOptionPane.showMessageDialog(new JFrame(), "User not found!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }catch (Exception e) {
                    System.out.println("Error!" + e.getMessage());
                } finally {
                    try {
                        // Close the result set, statement, and connection
                       if (resultset != null) {
                           resultset.close();
                       }
                       if (selectStatement != null) {
                           selectStatement.close();
                       }
                       if (connection != null) {
                           connection.close();
                       }
                   } catch (SQLException e) {
                       System.out.println("Error closing resources: " + e.getMessage());
                   }
                }
                
                // Check if the Desktop class is supported
                if (!Desktop.isDesktopSupported()) {
                    System.out.println("Not supported");
                    return false;
                } else {
                    // Open the selected file
                    Desktop desktop = Desktop.getDesktop();
                    desktop.open(selectedFile);
                    return true;
                }
            } else if (result == JFileChooser.CANCEL_OPTION) {
                // Handle the case when the user cancels the file selection
                System.out.println("Cancelled.");
                return false;
            }
        } catch (IOException e) {
            // Print the stack trace if an IOException occurs
            e.printStackTrace();
            return false;
        }
        return false;
    }
    
