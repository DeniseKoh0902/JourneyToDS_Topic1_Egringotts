    /**
     * Method to set user information in the UI components.
     * Retrieves user data from the Account object and sets it in corresponding fields.
     */
    public void setUser() {
        this.nameOfUser.setText(user.getUsername());
        this.userID.setText(user.getUserID());
        this.DOB.setText(user.getDOB());
        this.phoneNumber.setText(user.getTelephone());
        this.email.setText(user.getEmail());
        this.homeAddress.setText(user.getAddress());
        
        // Check if the user has a custom avatar image
        if (user.getUserAvatar() != null && user.getUserAvatar().getImagePath() != null && 
            !user.getUserAvatar().getImagePath().trim().equals("/Images/profilePic.jpg")) {
            this.imagePathString = user.getUserAvatar().getImagePath();
            profilePic.setIcon(new javax.swing.ImageIcon(this.imagePathString));
        } else {
            System.out.println("Please wait for the image path to parse in....");
        }
    }
    
