    /**
     * Reads the content of the "announcement.txt" file and displays it in the todayAnnouncement text area.
     * Displays an error message if the file cannot be read.
     */
    private void displayAnnouncement() {
        try {
            // Create a FileReader to read the announcement file
            FileReader reader = new FileReader("announcement.txt");
            // Wrap the FileReader in a BufferedReader for efficient reading
            BufferedReader bufferedReader = new BufferedReader(reader);
            StringBuilder stringBuilder = new StringBuilder();
            String line;
            
            // Read the file line by line and append each line to the StringBuilder
            while ((line = bufferedReader.readLine()) != null) {
                stringBuilder.append(line).append("\n");
            }
            
            // Set the text of the todayAnnouncement component to the content of the file
            todayAnnouncement.setText(stringBuilder.toString());
            // Close the BufferedReader
            bufferedReader.close();
        } catch (IOException ex) {
            // Display an error message if there is an issue reading the file
            JOptionPane.showMessageDialog(null, "Error reading file: " + ex.getMessage());
        }
    }

    
    
